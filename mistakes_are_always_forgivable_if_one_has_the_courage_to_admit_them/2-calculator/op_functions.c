int op_add(int a, int b) /* Function to the arithmetic sum.*/
{
    return ( a + b );
}

int op_sub(int a, int b) /* Function to the subtraction.*/
{
    return ( a - b );   
}

int op_mul(int a, int b) /* Function to multiplication. */
{
    return ( a * b );
}

int op_div(int a, int b) /* Function to the division.   */
{
    return ( a / b );
}

int op_mod(int a, int b) /* Function to the modulo op.  */
{
    return ( a % b );
}
